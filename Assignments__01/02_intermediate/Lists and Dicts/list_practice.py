def main():
    # Step 1: Create a list of fruits
    fruit_list = ['apple', 'banana', 'orange', 'grape', 'pineapple']
    
    # Step 2: Display the original list and its length
    print("Original fruit list:", fruit_list)
    print("Total number of fruits:", len(fruit_list))
    
    # Step 3: Add 'mango' to the end of the list
    fruit_list.append('mango')
    
    # Step 4: Show the updated list
    print("Updated fruit list after adding 'mango':", fruit_list)

# Entry point of the program
if __name__ == "__main__":
    main()
